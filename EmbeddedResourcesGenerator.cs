[Generator]
public class TemplateFilesGenerator : IIncrementalGenerator
{
    public void Initialize(IncrementalGeneratorInitializationContext context)
    {
        // Get all AdditionalText files
        var templates = context.AdditionalTextsProvider
            .Where(at => at.Path.EndsWith(".template", System.StringComparison.OrdinalIgnoreCase));

        // Map to (path, text)
        var fileContents = templates.Select((additionalText, ct) =>
        {
            var path = additionalText.Path;
            var text = additionalText.GetText(ct)?.ToString() ?? string.Empty;
            return (path, text);
        });

        // Create a generated source per template
        context.RegisterSourceOutput(fileContents, (spc, state) =>
        {
            var (path, text) = state;
            var className = SafeIdentifier(Path.GetFileNameWithoutExtension(path));
            var escaped = EscapeForVerbatimString(text);

            var sb = new StringBuilder();
            sb.AppendLine("namespace GeneratedFromTemplates");
            sb.AppendLine("{");
            sb.AppendLine($"    public static class {className}");
            sb.AppendLine("    {");
            sb.AppendLine($"        public const string Template = @\"{escaped}\";");
            sb.AppendLine("    }");
            sb.AppendLine("}");

            var hintName = $"{className}.g.cs";
            spc.AddSource(hintName, SourceText.From(sb.ToString(), Encoding.UTF8));
        });
    }

    static string SafeIdentifier(string raw)
    {
        if (string.IsNullOrEmpty(raw)) return "Template";
        var sb = new StringBuilder();
        foreach (var ch in raw)
            sb.Append(char.IsLetterOrDigit(ch) || ch == '_' ? ch : '_');
        return sb.ToString();
    }

    static string EscapeForVerbatimString(string s) => (s ?? string.Empty).Replace("\"", "\"\"");
}


