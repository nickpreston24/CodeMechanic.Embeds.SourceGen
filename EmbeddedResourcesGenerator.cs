public static class StringBuilderExtensions
{
    public static StringBuilder AsStringBuilder(this string text) => new StringBuilder(text);

    public static StringBuilder RemoveFromEnd(this StringBuilder sb, int number_of_characters) =>
        sb.Length >= number_of_characters ? sb.With(s => s.Length -= number_of_characters) : sb;

    public static StringBuilder Prepend(this StringBuilder sb, string line) => sb.Insert(0, line);

    /// <summary>
    /// Append only when a condition is met
    /// </summary>
    public static StringBuilder AppendIf<T>(
        this StringBuilder builder,
        Func<T, bool> predicate,
        params T[] values
    )
    {
        var list = values ?? Enumerable.Empty<T>();
        foreach (var value in list.Where(value => predicate(value)))
        {
            builder.Append(value);
        }

        return builder;
    }

    [Obsolete("Use the one without the space delimiter")]
    public static StringBuilder AppendEach<T>(
        this StringBuilder builder,
        IEnumerable<T> collection,
        Func<T, string> selector = null,
        string delimiter = " "
    )
    {
        return collection
            .Aggregate(
                builder,
                (_, next_value) =>
                {
                    selector = selector.TryGet(selector_fn => selector_fn);
                    string next_line = selector(next_value);
                    builder.Append($"{next_line}{delimiter}");
                    return builder;
                }
            )
            .RemoveFromEnd(delimiter.Length);
    }
}

public static class TypeExtensions
{
    /// <summary>
    /// Update properties on a long, nested object easily.
    ///
    /// Usage:
    ///   your.ultralarge.complex.infragistics.winforms.grid.table.row.With(row=> {
    ///      row.Text = "hello there!"
    ///      row.Color = "blue"
    ///      ...
    ///      };
    ///</summary>
    public static T With<T>(this T obj, Action<T> patch)
    {
        patch(obj);
        return obj;
    }


    /// <summary>
    /// (VS 2022/ 2015 SHIM)
    /// This is a replacement for the '?.' null coaslesce or 'Elvis operator'.
    /// This is here because VB can implicitly convert NULL object values like the Request["some_param"], but C# will not.
    /// This also prevents trying to make if-null-else logic in our ASPX files, and instead lets us call one method to make a type conversion, like so:
    ///
    /// <%= r["orderdatetime"].TryGet(odt=>odt.ToString().ToDateTime().Value) %>
    /// </summary>
    public static TValue TryGet<TObject, TValue>(
        this TObject obj,
        Func<TObject, TValue> getter,
        TValue fallback = default
    )
        where TObject : class
    {
        // TODO: this doesn't properly get the correct type on the lefthand side when TOject actually is a string.
        if (typeof(TObject) == typeof(string))
        {
            string value = obj as string;
            return string.IsNullOrWhiteSpace(value) ? fallback : getter(obj);
        }

        return obj == null ? fallback : getter(obj);
    }
}

/// <summary>
/// A series of VB string methods as C# extension methods
/// Credit: https://kodify.net/csharp/strings/left-right-mid/
/// </summary>
public static class StringExtensions
{
    public static bool IsEmpty(this string text) => string.IsNullOrWhiteSpace(text);

    public static bool IsNotEmpty(this string text) => !text.IsEmpty();

    public static bool NotEmpty(this string text) => IsNotEmpty(text);

    public static bool OverridesToString(this object v)
    {
        return v.ToString() != v.GetType().ToString();
    }

    public static string Rollup(this string[] lines_of_code)
    {
        return lines_of_code.Aggregate(
                new StringBuilder(),
                (sb, next) =>
                {
                    sb.AppendLine(next.TrimEnd());
                    return sb;
                })
            .ToString();
    }

    public static string Rollup(this string[] lines_of_code, string start,
        string end)
    {
        return lines_of_code.Aggregate(
                new StringBuilder(start),
                (sb, next) =>
                {
                    sb.AppendLine(next);
                    return sb;
                })
            .With(sb => sb.AppendLine(end))
            .ToString();
    }
}
